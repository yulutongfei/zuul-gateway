/**
 * @author 孙许
 * @version 1.0
 * @date 2021/10/20 22:49
 */
@SpringBootApplication
@EnableZuulProxy
public class ZuulGatewayApplication {

    public static void main(String[] args) {
        SpringApplication.run(ZuulGatewayApplication.class, args);
    }
}
